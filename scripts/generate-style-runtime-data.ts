import { mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { composeStyleRuntimePacksFromManifests } from '../components/recipes/stylePresetManifests';
import type {
  StyleRuntimePack,
  StyleRuntimePreset,
} from '../components/recipes/styles/runtimeTypes';
import { loadStyleManifestGraph, rootDir } from './style-manifest-files';

const outputPath = path.join(rootDir, 'components', 'recipes', 'styleRuntimeData.generated.ts');
const packOutputDir = path.join(rootDir, 'components', 'recipes', 'styleRuntimePacks.generated');
const checkOutputPath = path.join(
  rootDir,
  'components',
  'recipes',
  `styleRuntimeData.generated.check.${process.pid}.${Date.now()}.tmp.ts`,
);
const checkPackFileSuffix = `.check.${process.pid}.${Date.now()}.tmp.ts`;
const checkMode = process.argv.includes('--check');

const { graph, packManifests, presetManifests } = await loadStyleManifestGraph();

if (graph.errors.length > 0) {
  console.error(`[styles:runtime] graph errors=${graph.errors.length}`);
  for (const error of graph.errors) {
    console.error(`- ${error}`);
  }
  process.exit(1);
}

const packs: StyleRuntimePack[] = composeStyleRuntimePacksFromManifests(
  packManifests,
  presetManifests,
);

interface StyleRuntimeCategoryChunk {
  id: string;
  name: string;
  presets: StyleRuntimePreset[];
}

function slugify(value: string) {
  return value
    .trim()
    .toLowerCase()
    .replace(/^[0-9]+[.)]\s*/, '')
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function categoryChunksForPack(pack: StyleRuntimePack): StyleRuntimeCategoryChunk[] {
  const chunks = new Map<string, StyleRuntimeCategoryChunk>();
  const chunksByName = new Map<string, StyleRuntimeCategoryChunk>();

  for (const preset of pack.presets) {
    const name = preset.category?.trim() || 'General';
    const existing = chunksByName.get(name);
    if (existing) {
      existing.presets.push(preset);
      continue;
    }
    const baseId = slugify(name) || 'general';
    const id = `${baseId}-${chunks.size + 1}`;
    const chunk: StyleRuntimeCategoryChunk = { id, name, presets: [preset] };
    chunks.set(id, chunk);
    chunksByName.set(name, chunk);
  }

  return Array.from(chunks.values());
}

function buildIndexSource() {
  const summaries = packs.map(({ id, name, description, presets }) => ({
    id,
    name,
    description,
    presetCount: presets.length,
  }));
  const loaderEntries = packs
    .map(
      (pack) =>
        `  ${JSON.stringify(pack.id)}: () => import('./styleRuntimePacks.generated/${pack.id}').then((module) => module.loadGeneratedStyleRuntimePack()),`,
    )
    .join('\n');

  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Edit Style Preset Manifests, then run `bun run styles:runtime`.',
    "import type { StyleRuntimePack } from './styles/runtimeTypes';",
    '',
    'export interface GeneratedStyleRuntimePackSummary {',
    '  id: string;',
    '  name: string;',
    '  description: string;',
    '  presetCount: number;',
    '}',
    '',
    `export const GENERATED_STYLE_RUNTIME_PACK_SUMMARIES = ${JSON.stringify(summaries, null, 2)} as GeneratedStyleRuntimePackSummary[];`,
    '',
    'const GENERATED_STYLE_RUNTIME_PACK_LOADERS: Record<string, () => Promise<StyleRuntimePack>> = {',
    loaderEntries,
    '};',
    '',
    'export async function loadGeneratedStyleRuntimePack(packId: string): Promise<StyleRuntimePack | null> {',
    '  const loader = GENERATED_STYLE_RUNTIME_PACK_LOADERS[packId];',
    '  return loader ? loader() : null;',
    '}',
    '',
    'export async function loadGeneratedStyleRuntimePacks(): Promise<StyleRuntimePack[]> {',
    '  return Promise.all(GENERATED_STYLE_RUNTIME_PACK_SUMMARIES.map((pack) => loadGeneratedStyleRuntimePack(pack.id))).then((loaded) => loaded.filter(Boolean) as StyleRuntimePack[]);',
    '}',
    '',
  ].join('\n');
}

function buildPackSource(pack: StyleRuntimePack, importSuffix = '') {
  const chunks = categoryChunksForPack(pack);
  const loaderEntries = chunks
    .map(
      (chunk) =>
        `  () => import('./${pack.id}/${chunk.id}${importSuffix}').then((module) => module.GENERATED_STYLE_PRESETS),`,
    )
    .join('\n');

  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Edit Style Preset Manifests, then run `bun run styles:runtime`.',
    "import type { StyleRuntimePack, StyleRuntimePreset } from '../styles/runtimeTypes';",
    '',
    'const CATEGORY_PRESET_LOADERS: Array<() => Promise<StyleRuntimePreset[]>> = [',
    loaderEntries,
    '];',
    '',
    'export async function loadGeneratedStyleRuntimePack(): Promise<StyleRuntimePack> {',
    '  const categoryPresets = await Promise.all(CATEGORY_PRESET_LOADERS.map((loader) => loader()));',
    '  return {',
    `    id: ${JSON.stringify(pack.id)},`,
    `    name: ${JSON.stringify(pack.name)},`,
    `    description: ${JSON.stringify(pack.description)},`,
    '    presets: categoryPresets.flat(),',
    '  };',
    '}',
    '',
  ].join('\n');
}

function buildCategorySource(category: StyleRuntimeCategoryChunk) {
  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Edit Style Preset Manifests, then run `bun run styles:runtime`.',
    "import type { StyleRuntimePreset } from '../../styles/runtimeTypes';",
    '',
    `export const GENERATED_STYLE_PRESETS = ${JSON.stringify(category.presets, null, 2)} as unknown as StyleRuntimePreset[];`,
    '',
  ].join('\n');
}

async function formatGeneratedFiles(filePaths: string[]) {
  const batchSize = 20;
  for (let index = 0; index < filePaths.length; index += batchSize) {
    const batch = filePaths.slice(index, index + batchSize);
    const formatter = Bun.spawn(['bunx', 'vp', 'check', '--fix', ...batch], {
      stdout: 'pipe',
      stderr: 'pipe',
    });
    const [stdout, stderr, exitCode] = await Promise.all([
      new Response(formatter.stdout).text(),
      new Response(formatter.stderr).text(),
      formatter.exited,
    ]);

    if (stdout.trim()) console.log(stdout.trim());
    if (stderr.trim()) console.error(stderr.trim());
    if (exitCode !== 0) process.exit(exitCode);
  }
}

if (checkMode) {
  const packChunks = packs.flatMap((pack) =>
    categoryChunksForPack(pack).map((category) => ({ pack, category })),
  );
  const checkPackPaths = packs.map((pack) => path.join(packOutputDir, `${pack.id}.check.tmp.ts`));
  const checkCategoryPaths = packChunks.map(({ pack, category }) =>
    path.join(packOutputDir, pack.id, `${category.id}${checkPackFileSuffix}`),
  );
  try {
    await mkdir(packOutputDir, { recursive: true });
    await Promise.all(
      packs.map((pack) => mkdir(path.join(packOutputDir, pack.id), { recursive: true })),
    );
    await Promise.all([
      writeFile(checkOutputPath, buildIndexSource(), 'utf8'),
      ...packs.map((pack) =>
        writeFile(
          path.join(packOutputDir, `${pack.id}.check.tmp.ts`),
          buildPackSource(pack),
          'utf8',
        ),
      ),
      ...packChunks.map(({ pack, category }) =>
        writeFile(
          path.join(packOutputDir, pack.id, `${category.id}${checkPackFileSuffix}`),
          buildCategorySource(category),
          'utf8',
        ),
      ),
    ]);
    await formatGeneratedFiles([checkOutputPath, ...checkPackPaths, ...checkCategoryPaths]);
    const [expectedIndex, actualIndex] = await Promise.all([
      readFile(checkOutputPath, 'utf8'),
      readFile(outputPath, 'utf8'),
    ]);

    if (actualIndex !== expectedIndex) {
      console.error(
        `[styles:runtime] ${path.relative(rootDir, outputPath)} is stale. Run bun run styles:runtime.`,
      );
      process.exit(1);
    }

    let isStale = false;
    for (const pack of packs) {
      const actualPath = path.join(packOutputDir, `${pack.id}.ts`);
      const expectedPath = path.join(packOutputDir, `${pack.id}.check.tmp.ts`);
      const [expected, actual] = await Promise.all([
        readFile(expectedPath, 'utf8'),
        readFile(actualPath, 'utf8'),
      ]);
      if (actual !== expected) {
        console.error(
          `[styles:runtime] ${path.relative(rootDir, actualPath)} is stale. Run bun run styles:runtime.`,
        );
        isStale = true;
      }
    }

    for (const { pack, category } of packChunks) {
      const actualPath = path.join(packOutputDir, pack.id, `${category.id}.ts`);
      const expectedPath = path.join(
        packOutputDir,
        pack.id,
        `${category.id}${checkPackFileSuffix}`,
      );
      const [expected, actual] = await Promise.all([
        readFile(expectedPath, 'utf8'),
        readFile(actualPath, 'utf8'),
      ]);
      if (actual !== expected) {
        console.error(
          `[styles:runtime] ${path.relative(rootDir, actualPath)} is stale. Run bun run styles:runtime.`,
        );
        isStale = true;
      }
    }

    if (isStale) process.exitCode = 1;
  } finally {
    await rm(checkOutputPath, { force: true });
    await Promise.all(
      [...checkPackPaths, ...checkCategoryPaths].map((filePath) => rm(filePath, { force: true })),
    );
  }

  if (process.exitCode) process.exit(process.exitCode);
  console.log(`[styles:runtime] ${path.relative(rootDir, outputPath)} is current.`);
  console.log(
    `[styles:runtime] packs=${packs.length} presets=${packs.reduce((n, p) => n + p.presets.length, 0)}`,
  );
  process.exit(0);
}

await rm(packOutputDir, { recursive: true, force: true });
await mkdir(packOutputDir, { recursive: true });

const packOutputPaths = packs.map((pack) => path.join(packOutputDir, `${pack.id}.ts`));
const categoryOutputPaths = packs.flatMap((pack) =>
  categoryChunksForPack(pack).map((category) =>
    path.join(packOutputDir, pack.id, `${category.id}.ts`),
  ),
);
await Promise.all(
  packs.map((pack) => mkdir(path.join(packOutputDir, pack.id), { recursive: true })),
);
await Promise.all([
  writeFile(outputPath, buildIndexSource(), 'utf8'),
  ...packs.map((pack) =>
    writeFile(path.join(packOutputDir, `${pack.id}.ts`), buildPackSource(pack), 'utf8'),
  ),
  ...packs.flatMap((pack) =>
    categoryChunksForPack(pack).map((category) =>
      writeFile(
        path.join(packOutputDir, pack.id, `${category.id}.ts`),
        buildCategorySource(category),
        'utf8',
      ),
    ),
  ),
]);

await formatGeneratedFiles([outputPath, ...packOutputPaths, ...categoryOutputPaths]);

console.log(`[styles:runtime] wrote ${path.relative(rootDir, outputPath)}`);
console.log(
  `[styles:runtime] packs=${packs.length} presets=${packs.reduce((n, p) => n + p.presets.length, 0)}`,
);
