interface StyleRuntimeCategoryChunk {
  id: string;
  name: string;
  presets: StyleRuntimePreset[];
}

function slugify(value: string) {
  return value
    .trim()
    .toLowerCase()
    .replace(/^[0-9]+[.)]\s*/, '')
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function categoryChunksForPack(pack: StyleRuntimePack): StyleRuntimeCategoryChunk[] {
  const chunks = new Map<string, StyleRuntimeCategoryChunk>();
  const chunksByName = new Map<string, StyleRuntimeCategoryChunk>();

  for (const preset of pack.presets) {
    const name = preset.category?.trim() || 'General';
    const existing = chunksByName.get(name);
    if (existing) {
      existing.presets.push(preset);
      continue;
    }
    const baseId = slugify(name) || 'general';
    const id = `${baseId}-${chunks.size + 1}`;
    const chunk: StyleRuntimeCategoryChunk = { id, name, presets: [preset] };
    chunks.set(id, chunk);
    chunksByName.set(name, chunk);
  }

  return Array.from(chunks.values());
}

function buildIndexSource() {
  const summaries = packs.map(({ id, name, description, presets }) => ({
    id,
    name,
    description,
    presetCount: presets.length,
  }));
  const loaderEntries = packs
    .map(
      (pack) =>
        `  ${JSON.stringify(pack.id)}: () => import('./styleRuntimePacks.generated/${pack.id}').then((module) => module.loadGeneratedStyleRuntimePack()),`,
    )
    .join('\n');

  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Edit Style Preset Manifests, then run `bun run styles:runtime`.',
    "import type { StyleRuntimePack } from './styles/runtimeTypes';",
    '',
    'export interface GeneratedStyleRuntimePackSummary {',
    '  id: string;',
    '  name: string;',
    '  description: string;',
    '  presetCount: number;',
    '}',
    '',
    `export const GENERATED_STYLE_RUNTIME_PACK_SUMMARIES = ${JSON.stringify(summaries, null, 2)} as GeneratedStyleRuntimePackSummary[];`,
    '',
    'const GENERATED_STYLE_RUNTIME_PACK_LOADERS: Record<string, () => Promise<StyleRuntimePack>> = {',
    loaderEntries,
    '};',
    '',
    'export async function loadGeneratedStyleRuntimePack(packId: string): Promise<StyleRuntimePack | null> {',
    '  const loader = GENERATED_STYLE_RUNTIME_PACK_LOADERS[packId];',
    '  return loader ? loader() : null;',
    '}',
    '',
    'export async function loadGeneratedStyleRuntimePacks(): Promise<StyleRuntimePack[]> {',
    '  return Promise.all(GENERATED_STYLE_RUNTIME_PACK_SUMMARIES.map((pack) => loadGeneratedStyleRuntimePack(pack.id))).then((loaded) => loaded.filter(Boolean) as StyleRuntimePack[]);',
    '}',
    '',
  ].join('\n');
}

function buildPackSource(pack: StyleRuntimePack, importSuffix = '') {
  const chunks = categoryChunksForPack(pack);
  const loaderEntries = chunks
    .map(
      (chunk) =>
        `  () => import('./${pack.id}/${chunk.id}${importSuffix}').then((module) => module.GENERATED_STYLE_PRESETS),`,
    )
    .join('\n');

  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Edit Style Preset Manifests, then run `bun run styles:runtime`.',
    "import type { StyleRuntimePack, StyleRuntimePreset } from '../styles/runtimeTypes';",
    '',
    'const CATEGORY_PRESET_LOADERS: Array<() => Promise<StyleRuntimePreset[]>> = [',
    loaderEntries,
    '];',
    '',
    'export async function loadGeneratedStyleRuntimePack(): Promise<StyleRuntimePack> {',
    '  const categoryPresets = await Promise.all(CATEGORY_PRESET_LOADERS.map((loader) => loader()));',
    '  return {',
    `    id: ${JSON.stringify(pack.id)},`,
    `    name: ${JSON.stringify(pack.name)},`,
    `    description: ${JSON.stringify(pack.description)},`,
    '    presets: categoryPresets.flat(),',
    '  };',
    '}',
    '',
  ].join('\n');
}

function buildCategorySource(category: StyleRuntimeCategoryChunk) {
  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Edit Style Preset Manifests, then run `bun run styles:runtime`.',
    "import type { StyleRuntimePreset } from '../../styles/runtimeTypes';",
    '',
    `export const GENERATED_STYLE_PRESETS = ${JSON.stringify(category.presets, null, 2)} as unknown as StyleRuntimePreset[];`,
    '',
  ].join('\n');
}

function comparePresetIds(first: string, second: string) {
  const left = first.match(/^SP(\d+)-(\d+)$/);
  const right = second.match(/^SP(\d+)-(\d+)$/);
  if (!left || !right) return first.localeCompare(second);
  return Number(left[1]) - Number(right[1]) || Number(left[2]) - Number(right[2]);
}

async function loadStyleDefaultImageIds() {
  try {
    const entries = await readdir(defaultsDir, { withFileTypes: true });
    return entries
      .filter((entry) => entry.isFile())
      .map((entry) => entry.name.match(/^(SP\d{2}-\d{3})\.webp$/i)?.[1])
      .filter((presetId): presetId is string => Boolean(presetId))
      .sort(comparePresetIds);
  } catch {
    return [];
  }
}

interface StyleDefaultImageVariant {
  presetId: string;
  fileName: string;
}

async function loadStyleDefaultImageVariants(): Promise<StyleDefaultImageVariant[]> {
  try {
    const entries = await readdir(path.join(defaultsDir, 'variants'), { withFileTypes: true });
    return entries
      .filter((entry) => entry.isFile())
      .map((entry) => {
        const match = entry.name.match(/^(SP\d{2}-\d{3})[-_][a-z0-9-]+\.webp$/i);
        return match ? { presetId: match[1], fileName: entry.name } : null;
      })
      .filter((entry): entry is StyleDefaultImageVariant => Boolean(entry))
      .sort(
        (first, second) =>
          comparePresetIds(first.presetId, second.presetId) ||
          first.fileName.localeCompare(second.fileName),
      );
  } catch {
    return [];
  }
}

async function loadStaleDefaultImageIds() {
  let backlog = '';
  try {
    backlog = await readFile(staleDefaultImagesBacklogPath, 'utf8');
  } catch {
    return [];
  }

  return Array.from(backlog.matchAll(/^\|\s*(SP\d{2}-\d{3})\s*\|/gm), (match) => match[1]).sort(
    comparePresetIds,
  );
}

function buildStaleDefaultImagesSource(staleDefaultImageIds: string[]) {
  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Optionally track stale cards in .local/style-preset-card-regeneration-backlog.md, then run `bun run styles:runtime`.',
    '',
    `export const STALE_STYLE_DEFAULT_IMAGE_IDS = ${JSON.stringify(staleDefaultImageIds, null, 2)} as const;`,
    '',
    'const STALE_STYLE_DEFAULT_IMAGE_ID_SET = new Set<string>(STALE_STYLE_DEFAULT_IMAGE_IDS);',
    '',
    'export function isStyleDefaultImageStale(presetId: string) {',
    '  return STALE_STYLE_DEFAULT_IMAGE_ID_SET.has(presetId);',
    '}',
    '',
  ].join('\n');
}

function buildStyleDefaultImagesSource(
  styleDefaultImageIds: string[],
  styleDefaultImageVariants: StyleDefaultImageVariant[],
) {
  const imports = styleDefaultImageIds
    .map(
      (presetId, index) =>
        `import image${index} from '../assets/recipes/styles/defaults/${presetId}.webp?url';`,
    )
    .join('\n');
  const variantImports = styleDefaultImageVariants
    .map(
      (variant, index) =>
        `import variant${index} from '../assets/recipes/styles/defaults/variants/${variant.fileName}?url';`,
    )
    .join('\n');
  const entries = styleDefaultImageIds
    .map((presetId, index) => `  ${JSON.stringify(presetId)}: image${index},`)
    .join('\n');
  const variantEntriesByPresetId = new Map<string, string[]>();
  styleDefaultImageVariants.forEach((variant, index) => {
    variantEntriesByPresetId.set(variant.presetId, [
      ...(variantEntriesByPresetId.get(variant.presetId) ?? []),
      `variant${index}`,
    ]);
  });
  const variantEntries = Array.from(variantEntriesByPresetId.entries())
    .map(([presetId, values]) => `  ${JSON.stringify(presetId)}: [${values.join(', ')}],`)
    .join('\n');

  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Add/remove style default images, then run `bun run styles:runtime`.',
    '',
    imports,
    variantImports ? `\n${variantImports}` : '',
    '',
    'export const GENERATED_STYLE_DEFAULT_IMAGES: Record<string, string> = {',
    entries,
    '};',
    '',
    'export const GENERATED_STYLE_DEFAULT_IMAGE_VARIANTS: Record<string, string[]> = {',
    variantEntries,
    '};',
    '',
  ].join('\n');
}

async function formatGeneratedFiles(filePaths: string[]) {
  // Check mode can generate hundreds of temp files; formatting them one by one
  // makes `styles:runtime:check` effectively unusable in large repos.
  const batchSize = 20;
  for (let index = 0; index < filePaths.length; index += batchSize) {
    const batch = filePaths.slice(index, index + batchSize);
    const formatter = Bun.spawn(['bunx', 'vp', 'fmt', '--threads', '4', ...batch], {
      stdout: 'pipe',
      stderr: 'pipe',
    });
    const [stdout, stderr, exitCode] = await Promise.all([
      new Response(formatter.stdout).text(),
      new Response(formatter.stderr).text(),
      formatter.exited,
    ]);

    if (stdout.trim()) console.log(stdout.trim());
    if (stderr.trim()) console.error(stderr.trim());
    if (exitCode !== 0) {
      throw new Error(`vp fmt exited with code ${exitCode}`);
    }
  }
}

async function ensureCheckFilePresent(filePath: string, source: string) {
  try {
    await readFile(filePath, 'utf8');
    return;
  } catch {
    await writeFile(filePath, source, 'utf8');
    if (!skipFormat) {
      await formatGeneratedFiles([filePath]);
    }
  }
}

async function readCheckFileWithRecovery(filePath: string, source: string) {
  for (let attempt = 0; attempt < 3; attempt += 1) {
    try {
      return await readFile(filePath, 'utf8');
    } catch (error) {
      const code =
        error && typeof error === 'object' && 'code' in error ? String(error.code) : 'UNKNOWN';
      if (code !== 'ENOENT') throw error;

      await ensureCheckFilePresent(filePath, source);
      if (attempt < 2) {
        await Bun.sleep(25);
        continue;
      }
    }
  }

  await ensureCheckFilePresent(filePath, source);
  return readFile(filePath, 'utf8');
}

if (checkMode) {
  const packChunks = packs.flatMap((pack) =>
    categoryChunksForPack(pack).map((category) => ({ pack, category })),
  );
  const checkPackPaths = packs.map((pack) =>
    path.join(checkPackOutputDir, `${pack.id}.check.tmp.ts`),
  );
  const checkCategoryPaths = packChunks.map(({ pack, category }) =>
    path.join(checkPackOutputDir, pack.id, `${category.id}${checkPackFileSuffix}`),
  );
  const [staleDefaultImageIds, styleDefaultImageIds, styleDefaultImageVariants] = await Promise.all(
    [loadStaleDefaultImageIds(), loadStyleDefaultImageIds(), loadStyleDefaultImageVariants()],
  );
  try {
    await mkdir(checkPackOutputDir, { recursive: true });
    await Promise.all(
      packs.map((pack) => mkdir(path.join(checkPackOutputDir, pack.id), { recursive: true })),
    );
    await Promise.all([
      writeFile(checkOutputPath, buildIndexSource(), 'utf8'),
      writeFile(
        checkStaleDefaultImagesOutputPath,
        buildStaleDefaultImagesSource(staleDefaultImageIds),
        'utf8',
      ),
      writeFile(
        checkStyleDefaultImagesOutputPath,
        buildStyleDefaultImagesSource(styleDefaultImageIds, styleDefaultImageVariants),
        'utf8',
      ),
      ...packs.map((pack) =>
        writeFile(
          path.join(checkPackOutputDir, `${pack.id}.check.tmp.ts`),
          buildPackSource(pack),
          'utf8',
        ),
      ),
      ...packChunks.map(({ pack, category }) =>
        writeFile(
          path.join(checkPackOutputDir, pack.id, `${category.id}${checkPackFileSuffix}`),
          buildCategorySource(category),
          'utf8',
        ),
      ),
    ]);
    if (!skipFormat) {
      await formatGeneratedFiles([
        checkOutputPath,
        checkStaleDefaultImagesOutputPath,
        checkStyleDefaultImagesOutputPath,
        ...checkPackPaths,
        ...checkCategoryPaths,
      ]);
    }
    const [
      expectedIndex,
      actualIndex,
      expectedStaleDefaultImages,
      actualStaleDefaultImages,
      expectedStyleDefaultImages,
      actualStyleDefaultImages,
    ] = await Promise.all([
      readFile(checkOutputPath, 'utf8'),
      readFile(outputPath, 'utf8'),
      readFile(checkStaleDefaultImagesOutputPath, 'utf8'),
      readFile(staleDefaultImagesOutputPath, 'utf8'),
      readFile(checkStyleDefaultImagesOutputPath, 'utf8'),
      readFile(styleDefaultImagesOutputPath, 'utf8'),
    ]);

    if (actualIndex !== expectedIndex) {
      console.error(
        `[styles:runtime] ${path.relative(rootDir, outputPath)} is stale. Run bun run styles:runtime.`,
      );
      process.exit(1);
    }

    if (actualStaleDefaultImages !== expectedStaleDefaultImages) {
      console.error(
        `[styles:runtime] ${path.relative(rootDir, staleDefaultImagesOutputPath)} is stale. Run bun run styles:runtime.`,
      );
      process.exit(1);
    }

    if (actualStyleDefaultImages !== expectedStyleDefaultImages) {
      console.error(
        `[styles:runtime] ${path.relative(rootDir, styleDefaultImagesOutputPath)} is stale. Run bun run styles:runtime.`,
      );
      process.exit(1);
    }

    let isStale = false;
    for (const pack of packs) {
      const actualPath = path.join(packOutputDir, `${pack.id}.ts`);
      const expectedPath = path.join(checkPackOutputDir, `${pack.id}.check.tmp.ts`);
      const expectedSource = buildPackSource(pack);
      await ensureCheckFilePresent(expectedPath, expectedSource);
      const [expected, actual] = await Promise.all([
        readCheckFileWithRecovery(expectedPath, expectedSource),
        readFile(actualPath, 'utf8'),
      ]);
      if (actual !== expected) {
        console.error(
          `[styles:runtime] ${path.relative(rootDir, actualPath)} is stale. Run bun run styles:runtime.`,
        );
        isStale = true;
      }
    }

    for (const { pack, category } of packChunks) {
      const actualPath = path.join(packOutputDir, pack.id, `${category.id}.ts`);
      const expectedPath = path.join(
        checkPackOutputDir,
        pack.id,
        `${category.id}${checkPackFileSuffix}`,
      );
      const expectedSource = buildCategorySource(category);
      await ensureCheckFilePresent(expectedPath, expectedSource);
      const [expected, actual] = await Promise.all([
        readCheckFileWithRecovery(expectedPath, expectedSource),
        readFile(actualPath, 'utf8'),
      ]);
      if (actual !== expected) {
        console.error(
          `[styles:runtime] ${path.relative(rootDir, actualPath)} is stale. Run bun run styles:runtime.`,
        );
        isStale = true;
      }
    }

    if (isStale) process.exitCode = 1;
  } finally {
    await rm(checkTempRoot, { recursive: true, force: true });
  }

  if (process.exitCode) process.exit(process.exitCode);
  console.log(`[styles:runtime] ${path.relative(rootDir, outputPath)} is current.`);
  console.log(
    `[styles:runtime] packs=${packs.length} presets=${packs.reduce((n, p) => n + p.presets.length, 0)}`,
  );
  process.exit(0);
}

