import { readFile, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { composeStylePacksFromManifests } from '../components/recipes/stylePresetManifests';
import type { StylePack } from '../components/recipes/styles/types';
import { loadStyleManifestGraph, rootDir } from './style-manifest-files';

const outputPath = path.join(rootDir, 'components', 'recipes', 'styleRuntimeData.generated.ts');
const checkOutputPath = path.join(
  rootDir,
  'components',
  'recipes',
  `styleRuntimeData.generated.check.${process.pid}.${Date.now()}.tmp.ts`,
);
const checkMode = process.argv.includes('--check');

const { graph, packManifests, presetManifests } = await loadStyleManifestGraph();

if (graph.errors.length > 0) {
  console.error(`[styles:runtime] graph errors=${graph.errors.length}`);
  for (const error of graph.errors) {
    console.error(`- ${error}`);
  }
  process.exit(1);
}

const packs: StylePack[] = composeStylePacksFromManifests(packManifests, presetManifests);

function buildGeneratedSource() {
  return [
    '// Generated by scripts/generate-style-runtime-data.ts.',
    '// Edit Style Preset Manifests, then run `bun run styles:runtime`.',
    "import type { StylePack } from './styles/types';",
    '',
    `export const GENERATED_STYLE_PACKS = ${JSON.stringify(packs, null, 2)} as StylePack[];`,
    '',
  ].join('\n');
}

async function formatGeneratedFile(filePath: string) {
  const formatter = Bun.spawn(['bunx', 'vp', 'check', '--fix', filePath], {
    stdout: 'pipe',
    stderr: 'pipe',
  });
  const [stdout, stderr, exitCode] = await Promise.all([
    new Response(formatter.stdout).text(),
    new Response(formatter.stderr).text(),
    formatter.exited,
  ]);

  if (stdout.trim()) console.log(stdout.trim());
  if (stderr.trim()) console.error(stderr.trim());
  if (exitCode !== 0) process.exit(exitCode);
}

if (checkMode) {
  try {
    await writeFile(checkOutputPath, buildGeneratedSource(), 'utf8');
    await formatGeneratedFile(checkOutputPath);
    const [expected, actual] = await Promise.all([
      readFile(checkOutputPath, 'utf8'),
      readFile(outputPath, 'utf8'),
    ]);

    if (actual !== expected) {
      console.error(
        `[styles:runtime] ${path.relative(rootDir, outputPath)} is stale. Run bun run styles:runtime.`,
      );
      process.exit(1);
    }
  } finally {
    await rm(checkOutputPath, { force: true });
  }

  console.log(`[styles:runtime] ${path.relative(rootDir, outputPath)} is current.`);
  console.log(
    `[styles:runtime] packs=${packs.length} presets=${packs.reduce((n, p) => n + p.presets.length, 0)}`,
  );
  process.exit(0);
}

await writeFile(outputPath, buildGeneratedSource(), 'utf8');

await formatGeneratedFile(outputPath);

console.log(`[styles:runtime] wrote ${path.relative(rootDir, outputPath)}`);
console.log(
  `[styles:runtime] packs=${packs.length} presets=${packs.reduce((n, p) => n + p.presets.length, 0)}`,
);
