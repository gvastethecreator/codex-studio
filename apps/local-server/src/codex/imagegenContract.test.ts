import { describe, expect, it } from 'vite-plus/test';

import {
  buildCodexImagegenDeveloperInstructions,
  buildCodexImagegenFallbackPrompt,
  CODEX_IMAGEGEN_SESSION_CONTRACT,
} from './imagegenContract';

describe('imagegenContract', () => {
  it('keeps Codex imagegen stable instructions in one reusable contract', () => {
    expect(CODEX_IMAGEGEN_SESSION_CONTRACT).toMatchObject({
      id: 'codex-imagegen-v1',
      providerId: 'codex',
    });
    expect(CODEX_IMAGEGEN_SESSION_CONTRACT.stableInstructions).toContain(
      'Generate exactly one image for the current job.',
    );
  });

  it('builds developer instructions from the session contract', () => {
    const instructions = buildCodexImagegenDeveloperInstructions('pack_01');

    expect(instructions).toContain('persistent image generation thread (pack_01)');
    expect(instructions).toContain('Use the provided imagegen skill.');
    expect(instructions).toContain('Report only the image generated by the current turn.');
  });

  it('builds the legacy fallback prompt from the same stable contract', () => {
    const prompt = buildCodexImagegenFallbackPrompt('small brass key');

    expect(prompt).toContain('Generate exactly one image for the current job.');
    expect(prompt).toContain('Prompt:\nsmall brass key');
  });
});
