import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { CodexRpcClient } from './rpcClient';
import { resolveLibraryPath } from '../library';
import { log } from '../logger';

export interface SessionHandle {
  client: CodexRpcClient;
  codexHome: string | null;
  threadId: string | null;
  sessionKey: string;
  queue: Promise<void>;
}

export interface SessionPool {
  getOrCreateSession(projectId: string): Promise<SessionHandle>;
  releaseSession(handle: SessionHandle): void;
  destroySession(threadId: string): Promise<void>;
}

interface PersistedImagegenSession {
  sessionKey: string;
  threadId: string | null;
  updatedAt: string;
}

const imagegenSessions = new Map<string, SessionHandle>();

function getImagegenSessionRegistryPath() {
  return resolveLibraryPath('state', 'imagegen-session-registry.json');
}

function loadPersistedImagegenSessions() {
  const imagegenSessionRegistryPath = getImagegenSessionRegistryPath();
  if (!existsSync(imagegenSessionRegistryPath)) return new Map<string, PersistedImagegenSession>();
  try {
    const parsed = JSON.parse(readFileSync(imagegenSessionRegistryPath, 'utf8')) as PersistedImagegenSession[];
    return new Map(parsed.map((entry) => [entry.sessionKey, entry]));
  } catch {
    return new Map<string, PersistedImagegenSession>();
  }
}

function savePersistedImagegenSessions(sessions: Map<string, PersistedImagegenSession>) {
  const imagegenSessionRegistryPath = getImagegenSessionRegistryPath();
  mkdirSync(path.dirname(imagegenSessionRegistryPath), { recursive: true });
  const entries = [...sessions.values()].sort((a, b) => a.sessionKey.localeCompare(b.sessionKey));
  writeFileSync(imagegenSessionRegistryPath, `${JSON.stringify(entries, null, 2)}\n`, 'utf8');
}

function rememberImagegenSession(sessionKey: string, threadId: string | null) {
  const sessions = loadPersistedImagegenSessions();
  sessions.set(sessionKey, {
    sessionKey,
    threadId,
    updatedAt: new Date().toISOString(),
  });
  savePersistedImagegenSessions(sessions);
}

function forgetImagegenSession(sessionKey: string) {
  const sessions = loadPersistedImagegenSessions();
  if (!sessions.delete(sessionKey)) return;
  savePersistedImagegenSessions(sessions);
}

function getPersistedImagegenThreadId(sessionKey: string) {
  return loadPersistedImagegenSessions().get(sessionKey)?.threadId ?? null;
}

function normalizeSessionToken(value: string | null) {
  return (value ?? 'unknown')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9_-]+/g, '_')
    .replace(/^_+|_+$/g, '');
}

function extractPromptField(prompt: string, field: 'PACK' | 'CATEGORY') {
  const match = prompt.match(new RegExp(`^${field}:\\s*(.+)$`, 'im'));
  return match?.[1]?.trim() ?? null;
}

export function getImagegenSessionKey(prompt: string) {
  const pack = normalizeSessionToken(extractPromptField(prompt, 'PACK'));
  return pack || 'unknown_pack';
}

export async function createImagegenSession(sessionKey: string): Promise<SessionHandle> {
  const client = new CodexRpcClient();
  await client.connect();

  const init = await client.request('initialize', {
    clientInfo: {
      name: 'codex-image-studio',
      title: 'Codex Image Studio',
      version: '0.1.0',
    },
    capabilities: null,
  });
  const codexHome = init?.codexHome ?? null;
  client.notify('initialized');

  const persistedThreadId = getPersistedImagegenThreadId(sessionKey);
  let threadId = persistedThreadId;

  if (!threadId) {
    const thread = await client.request('thread/start', {
      model: process.env.CODEX_IMAGEGEN_MODEL || 'gpt-5.3-codex-spark',
      cwd: process.cwd(),
      approvalPolicy: 'never',
      sandbox: 'danger-full-access',
      sessionStartSource: 'startup',
      developerInstructions:
        `You are running inside Codex Image Studio in a persistent style-pack thread (${sessionKey}). ` +
        'Generate exactly one image per user turn through the provided imagegen skill. ' +
        'Treat every turn independently and use only the current user prompt for the requested style card. ' +
        'Do not run shell commands to locate or copy the newest generated image. ' +
        'Do not copy files from the Codex generated_images directory manually. ' +
        'Report only the image generated by the current turn.',
    });
    threadId = thread?.thread?.id ?? null;
  }

  const session: SessionHandle = {
    client,
    codexHome,
    threadId,
    sessionKey,
    queue: Promise.resolve(),
  };
  imagegenSessions.set(sessionKey, session);
  rememberImagegenSession(sessionKey, session.threadId);
  log('info', 'codex-session', `${persistedThreadId ? 'Reused' : 'Started'} persistent imagegen thread ${session.threadId ?? 'unknown'} for ${sessionKey}`);
  return session;
}

export function closeImagegenSession(sessionKey: string, options?: { invalidatePersistedThread?: boolean }) {
  const session = imagegenSessions.get(sessionKey);
  if (!session) return;
  session.client.close();
  imagegenSessions.delete(sessionKey);
  if (options?.invalidatePersistedThread) {
    forgetImagegenSession(sessionKey);
  }
}

export async function getImagegenSession(sessionKey: string) {
  const existing = imagegenSessions.get(sessionKey);
  if (existing) return existing;
  return createImagegenSession(sessionKey);
}

export function createSessionPool(): SessionPool {
  return {
    getOrCreateSession: getImagegenSession,
    releaseSession() {},
    async destroySession(threadIdOrSessionKey) {
      const sessionKey = imagegenSessions.has(threadIdOrSessionKey)
        ? threadIdOrSessionKey
        : [...imagegenSessions.values()].find((session) => session.threadId === threadIdOrSessionKey)?.sessionKey;
      if (sessionKey) closeImagegenSession(sessionKey, { invalidatePersistedThread: true });
    },
  };
}
